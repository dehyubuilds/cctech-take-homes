        <ul>
          <li>Reproducing copyrighted content without authorization</li>
          <li>Distributing copyrighted material without permission</li>
          <li>Creating derivative works without consent</li>
          <li>Publicly performing copyrighted works without license</li>
        </ul>

        <h3>3.2 How to Report Infringement</h3>
        <p>If you believe your copyright has been infringed on Twilly, please submit a DMCA takedown notice containing:</p>
        <p><strong>Required Information:</strong></p>
        <ol>
          <li><strong>Copyright Owner Information</strong>
            <ul>
              <li>Your name and contact information</li>
              <li>Your relationship to the copyrighted work</li>
              <li>Your electronic signature</li>
            </ul>
          </li>
          <li><strong>Copyrighted Work Description</strong>
            <ul>
              <li>Detailed description of the original work</li>
              <li>Title, author, and publication information</li>
              <li>Registration number (if applicable)</li>
            </ul>
          </li>
          <li><strong>Infringing Content Identification</strong>
            <ul>
              <li>Specific URLs or locations of infringing content</li>
              <li>Clear description of where the content appears</li>
              <li>Timestamps or episode information if applicable</li>
            </ul>
          </li>
          <li><strong>Good Faith Statement</strong>
            <ul>
              <li>Statement that you believe the use is not authorized</li>
              <li>Confirmation that the information is accurate</li>
              <li>Statement that you are authorized to act on behalf of the copyright owner</li>
            </ul>
          </li>
        </ol>

